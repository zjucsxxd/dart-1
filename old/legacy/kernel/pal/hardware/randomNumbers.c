#include "common.h"

u32int randSeed = 0;

u32int random(u32int seed, u32int max)
{
    u32int tmpmax = max+1;
	randSeed = randSeed+seed * 1103515245 +12345;
	return (u32int)(randSeed / 65536) % tmpmax; 
}
